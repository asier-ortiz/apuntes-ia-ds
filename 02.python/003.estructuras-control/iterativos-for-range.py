# range con un parámetro: fin (excluido)
for i in range(10):
    print(i)

# range con dos parámetros: inicio (incluido) y fin (excluido)
for i in range(0, 10):
    print(i)

# range con tres parámetros: inicio, fin y step
for i in range(0, 10, 2):
    print(i)

