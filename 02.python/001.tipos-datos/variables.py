
# Las variables se pueden declarar de forma independiente:

variable1 = 3
variable2 = 'Hola'
variable3 = 'Mundo'
variable4 = 5.7
variable5 = 5 + 6j
variable6 = True

print(variable1)
print(variable2)
print(variable3)
print(variable4)
print(variable5)
print(variable6)

# También se pueden declarar en una misma línea:
var1, var2, var3 = 'Hola', 'Mundo', "Qué tal"
print(var1)
print(var2)
print(var3)

# Podemos imprimirlas también en una misma línea separadas de espacio
print(var1 + " " + var2 + " " + var3)