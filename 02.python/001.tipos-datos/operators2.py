

# Operadores de asignacion

# usamos = para asignar un valor a una variable
x = 5

# Combinar operadores ariméticos, de comparacion y bit a bit
# con el operador de asignación para abreviar

# Equivale a x = x + 5
x += 5
# Equivale a x = x - 5
x -= 5
# Equivale a x = x * 5
x *= 5
# Equivale a x = x / 5
x /= 5
# Equivale a x = x % 5
x %= 5
# Equivale a x = x // 5
x //= 5
# Equivale a x = x ** 5
x **= 5
# Equivale a x = x & 5
x &= 5
# Equivale a x = x | 5
x |= 5
# Equivale a x = x ^ 5
x ^= 5
# Equivale a x = x >> 5
x >>= 5
# Equivale a x = x << 5
x <<= 5
