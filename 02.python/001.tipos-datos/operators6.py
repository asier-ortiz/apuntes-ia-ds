
x = 101
k = 11

# Operadores de bit, permiten realizar operaciones con los números en 
# representación binaria.

# verifica si el valor de x está contenido en k y devuelve True o False
print(x & k)

# verifica si el valor de x no está contenido en k y devuelve True o False
print(x | k)
print(x ^ k)
print(x << k)
print(x >> k)