
x = 5
y = 7

# Operadores de comparacion. Los utilizamos para 
# verificar si se cumplen condiciones entre las variables
# el resultado es un valor booleano True o False

# verifica si x es igual a y
result = x == y
# verifica si x es distinto a y
x != y
# verifica si x es mayor que y
x > y
# verifica si x es menor que y
x < y
# verifica si x es mayor o igual que y
x >= y
# verifica si x es menor o igual que y
x <= y
