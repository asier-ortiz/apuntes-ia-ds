# números enteros
entero1 = 1
entero2 = -1

# números decimales
decimal1 = 25.5
decimal2 = -25.5
print(decimal2)

# números complejos
complex1 = 3 + 6j
complex2 = -3 - 9j

print(type(entero1)) # int
print(type(entero2)) # int
print(type(decimal1)) # float
print(type(decimal2))
print(type(complex1))
print(type(complex2))