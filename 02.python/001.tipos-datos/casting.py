
# Conversiones a Integer
x = int(2)
y = int(2.5)
z = int ("34")

print(x)
print(y)
print(z)

# Conversiones a float
x = float(1)
y = int(2.5)
z = int("34")
w = int(34.4)

print(x)
print(y)
print(z)
print(w)

# Conversiones a string
x = str(1)
y = str(2.5)
z = str("Hola")

print(x)
print(y)
print(z)
